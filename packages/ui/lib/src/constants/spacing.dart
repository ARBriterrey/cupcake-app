/// Standard spacing constants for consistent layout
///
/// Use these instead of magic numbers to maintain consistency
/// across the application and make it easier to adjust spacing globally.
class Spacing {
  const Spacing._();

  /// Extra small spacing (4px)
  static const double xs = 4.0;

  /// Small spacing (8px)
  static const double sm = 8.0;

  /// Medium spacing (16px) - Default for most layouts
  static const double md = 16.0;

  /// Large spacing (24px)
  static const double lg = 24.0;

  /// Extra large spacing (32px)
  static const double xl = 32.0;

  /// Extra extra large spacing (48px)
  static const double xxl = 48.0;

  /// Huge spacing (64px) - For major section dividers
  static const double huge = 64.0;
}
