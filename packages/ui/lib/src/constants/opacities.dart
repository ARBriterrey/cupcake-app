/// Standard opacity constants for consistent visual hierarchy
///
/// Use these for text and UI elements to maintain consistent opacity levels.
class Opacities {
  const Opacities._();

  /// Disabled state (38%)
  static const double disabled = 0.38;

  /// Secondary text (60%)
  static const double secondary = 0.6;

  /// Subtle emphasis (87%)
  static const double subtle = 0.87;

  /// Full opacity (100%)
  static const double full = 1.0;
}
